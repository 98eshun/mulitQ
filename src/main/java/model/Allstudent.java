package model;

public class Allstudent {

		private String id;
		private String passwd;
		private String name;
		private String gender;
		private int KO;
		private int EN;
		private int MT;
		private int SI;
		
		public Allstudent() {			
		}
		
		public String getId() {
			return id;
		}

		public void setId(String id) {
			this.id = id;
		}

		public String getPasswd() {
			return passwd;
		}

		public void setPasswd(String passwd) {
			this.passwd = passwd;
		}

		public String getName() {
			return name;
		}

		public void setName(String name) {
			this.name = name;
		}

		public String getGender() {
			return gender;
		}

		public void setGender(String gender) {
			this.gender = gender;
		}
			
		public int getKO() {
			return KO;
		}

		public void setKO(int kO) {
			KO = kO;
		}

		public int getEN() {
			return EN;
		}

		public void setEN(int eN) {
			EN = eN;
		}

		public int getMT() {
			return MT;
		}

		public void setMT(int mT) {
			MT = mT;
		}

		public int getSI() {
			return SI;
		}

		public void setSI(int sI) {
			SI = sI;
		}
	}

